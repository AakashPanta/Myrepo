def bootstrap(project_name: str, template_name: str):
    templates = {"python-cli", "python-library", "docs", "automation-tool"}

    if template_name not in templates:
        print(f"Unknown template: {template_name}")
        print("Available templates: python-cli, python-library, docs, automation-tool")
        return

    base = Path(project_name)

    if base.exists():
        print(f"Project already exists: {project_name}")
        return

    base.mkdir()
    write_common_files(base, project_name)

    if template_name == "python-cli":
        bootstrap_python_cli(base, project_name)
    elif template_name == "python-library":
        bootstrap_python_library(base, project_name)
    elif template_name == "docs":
        bootstrap_docs(base, project_name)
    elif template_name == "automation-tool":
        bootstrap_automation_tool(base, project_name)

    print(f"Bootstrap complete: {project_name}")
    print(f"Template used: {template_name}")
if __name__ == "__main__":
    if len(sys.argv) < 2:
        show_help()
    else:
        command = sys.argv[1].lower()

        if command == "run":
            run()
        elif command == "version":
            version()
        elif command == "info":
            info()
        elif command == "status":
            status()
        elif command == "status-json":
            status_json()
        elif command == "config":
            config()
        elif command == "init":
            init()
        elif command == "doctor":
            doctor()
        elif command == "bootstrap":
            if len(sys.argv) < 4:
                print("Usage: python3 -m src.cli bootstrap <project-name> <template>")
                print("Available templates: python-cli, python-library, docs, automation-tool")
            else:
                bootstrap(sys.argv[2], sys.argv[3])
        elif command == "help":
            show_help()
        else:
            print(f"Unknown command: {command}")
            show_help()
